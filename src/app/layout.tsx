import './globals.css';
import 'swiper/css';
import 'swiper/css/zoom';
import 'swiper/css/navigation';
import 'swiper/css/pagination';
import 'react-datepicker/dist/react-datepicker.css';
import { Poppins } from 'next/font/google';
import { MenuBar } from '../components/MenuBar';
import { cn } from '@/lib/cn';
import { ResponsiveContainer } from '@/components/ui/ResponsiveContainer';
import { getServerUser } from '@/lib/getServerUser';
import { Providers } from '@/components/Providers';

const poppins = Poppins({
  weight: ['400', '500', '600', '700'],
  subsets: ['latin'],
});

export const metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const [user] = await getServerUser();
  return (
    <html lang="en" className="dark overflow-y-scroll">
      <head>
        <meta
          name="viewport"
          content="width=device-width, initial-scale=1, maximum-scale=1, user-scalable=0"
        />
      </head>
      <body className={cn('bg-background text-foreground', poppins.className)}>
        <Providers>
          {user ? (
            <>
              <div className="md:flex md:justify-center md:gap-2">
                <MenuBar />

                <ResponsiveContainer className="pb-20 md:pb-4">
                  {children}
                </ResponsiveContainer>
              </div>
            </>
          ) : (
            children
          )}
        </Providers>
      </body>
    </html>
  );
}
